public class Curso {
	
	private Integer codCurso;
	private String nombre;
	public Integer getCodCurso() {
		return codCurso;
	}
	public void setCodCurso(Integer codCurso) {
		this.codCurso = codCurso;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
}
